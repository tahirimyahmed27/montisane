export function LanguageProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>("fr");
  const [hydrated, setHydrated] = useState(false);

  useEffect(() => {
    try {
      const stored = localStorage.getItem(STORAGE_KEY) as Lang | null;
      if (stored === "fr" || stored === "en") setLangState(stored);
    } catch {}
    setHydrated(true);
  }, []);

  const setLang = (l: Lang) => {
    setLangState(l);
    try {
      localStorage.setItem(STORAGE_KEY, l);
    } catch {}
    if (typeof document !== "undefined") document.documentElement.lang = l;
  };

  useEffect(() => {
    if (hydrated && typeof document !== "undefined") document.documentElement.lang = lang;
  }, [lang, hydrated]);

  const t = (key: string) => {
    const v = (dict[lang] as Record<string, string>)[key];
    return v ?? key;
  };
  const loc = (v: Loc | string) => (typeof v === "string" ? v : v[lang]);
  const locArr = (a: (Loc | string)[]) => a.map(loc);

  return (
    <I18nContext.Provider value={{ lang, setLang, t, loc, locArr }}>{children}</I18nContext.Provider>
  );
}

export function useI18n() {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error("useI18n must be used inside LanguageProvider");
  return ctx;
}
